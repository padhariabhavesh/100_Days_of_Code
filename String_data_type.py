# Slice
name = "Gangster Gangster Gangster"
print(name)
print(name[6])
# Multiple indexing
print(name[0:3])
# [ Start: End: Stepsize]
print(name[0:5:2])
# Go till the end
print(name[0::5])

# What is indexing and string indexing
'''
Indexing goes to left to right in positve indexing and right to left in negative indexing
'''
print(name[-3])
# Multiple indexing
print(name[0:-4])
# [ Start: End: Stepsize]
print(name[1:6:3])
