# Built in methods

name = "Gangster is home and i am happy"
print(name)

# Convert to lower case
print(name.lower())

# Convert to upper case
print(name.upper())

# Return string with leading and trailing whitespace removed
print(name.strip())

# Split the  character
print(name.split())

# Find the index value of character position
print(name.find('home'))

# Replace the character
print(name.replace('is','was'))

# Start with specific character
print(name.startswith('i'))
print(name.startswith('G'))

# End with specific character
print(name.endswith('h'))
print(name.endswith('y'))

